#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a = 16;
    int b = 3;

    /* Opérations */
    printf("Addition : %d\n", a + b);
    printf("Soustraction : %d\n", a - b);
    printf("Multiplication : %d\n", a * b);
    printf("Division : %d\n", a / b);
    printf("Modulo : %d\n\n", a % b);

    /* Opérateurs de comparaison (résultat booléen : 0 ou 1) */
    printf("a égale à b ? : %d\n", a == b);
    printf("a plus petit que b ? : %d\n", a > b);

    return 0;
}





